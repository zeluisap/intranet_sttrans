<?php
class TbPortalLayout extends Escola_Tabela {
	protected $_name = "portal_layout";
	protected $_rowClass = "PortalLayout";
	protected $_dependentTables = array("TbSistema");
	
	public function getPorChave($chave) {
		$uss = $this->fetchAll(" chave = '{$chave}' ");
		if ($uss->count()) {
			return $uss->current();
		}
		return false;
	}
	
	public function listar() {
		$sql = $this->select();
		$sql->order("descricao");
		$rg = $this->fetchAll($sql);
		if (count($rg)) {
			return $rg;
		}
		return false;
	}
}